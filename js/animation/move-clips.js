function pose(overrides = {}) {
  const bodyAngle = overrides.bodyAngle ?? 0;
  return {
    root: [0, -20],
    bodyAngle,
    pelvisAngle: overrides.pelvisAngle ?? bodyAngle * 0.62,
    chestAngle: overrides.chestAngle ?? bodyAngle,
    chestShift: 0,
    headOffset: [0, -13.2],
    headFollow: 1,
    headScale: 1,
    headAngle: 0,
    leftPaw: [-10.5, -19],
    rightPaw: [10.5, -19],
    leftFoot: [-6, 0],
    rightFoot: [6, 0],
    leftHandAngle: Math.PI,
    rightHandAngle: 0,
    leftFootAngle: Math.PI,
    rightFootAngle: 0,
    leftArmBend: -1,
    rightArmBend: 1,
    leftLegBend: 1,
    rightLegBend: -1,
    leftArmFlip: 0,
    rightArmFlip: 0,
    leftLegFlip: 0,
    rightLegFlip: 0,
    squash: 0,
    stretch: 0,
    earFlutter: 0,
    tailAngle: -0.4,
    depthFront: "right",
    expression: "focus",
    smear: 0,
    ...overrides,
  };
}

function frame(at, value, label = "") {
  return { at, pose: pose(value), label };
}

function clip(cadence, frames) {
  return { cadence, frames };
}

